# 定义一个类“张飞”
class ZhangFei:
    # 属性如下：
    skin = "black"
    weapon = "zhangbashemao"
    weight = 200
    age = 36
    # 定义方法
    def cry(self):
        print("长坂坡吓退曹兵")
    def brother(self):
        print("桃园三结义")
    def fight(self):
        print("三英大战吕布")
# 实例化类
zf = ZhangFei()
# 调用方法
zf.fight()
